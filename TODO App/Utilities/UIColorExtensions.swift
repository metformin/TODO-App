    }
    static var appLightBlue: UIColor {
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1.0)
    }
    static var appLightGrey: UIColor {
        return UIColor(red: 202/255, green: 255/255, blue: 191/255, alpha: 1.0)
    }
}



